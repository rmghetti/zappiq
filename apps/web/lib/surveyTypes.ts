// ============================================================
// surveyTypes.ts — Tipos e blocos globais do survey de onboarding
// Cada bloco contém perguntas profundas para treinar a IA
// de atendimento de qualquer segmento de negócio.
// ============================================================

export interface SurveyQuestion {
  id: string;
  label: string;
  type: 'text' | 'textarea' | 'select' | 'multiselect' | 'boolean' | 'number' | 'phone' | 'url' | 'email';
  options?: string[];
  placeholder?: string;
  required?: boolean;
  helpText?: string;
  category?: string;
}

export interface SurveyBlock {
  id: string;
  title: string;
  description: string;
  icon: string;
  questions: SurveyQuestion[];
}

export interface SubSegment {
  key: string;
  label: string;
  description: string;
}

export interface Segment {
  key: string;
  icon: string;
  label: string;
  description: string;
  subsegments: SubSegment[];
}

